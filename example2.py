# = Asigna el valor a la variable
x = 5  
print(x)


# += Suma el valor a la derecha del igual a la variable X1
x1 = 5; x1 += 2
print(x1)


# -= Resta el valor a la derecha del igual a la variable X2
x2 = 5; x2 -= 2
print(x2)



# *= Multiplica el valor a la derecha del igual a la variable X3
x3 = 5; x3 *= 2
print(x3)

# /= Divide el valor a la derecha del igual a la variable X4
x4 = 5; x4 /= 2
print(x4)


# %= Devuelve el resto de la división del valor a la derecha del igual con la variable X5
x5 = 5; x5 %= 2
print(x5)


# //= Devuelve el cociente de la división del valor a la derecha del igual con la variable X6
x6 = 5; x6 //= 2
print(x6)


#  **= Calcula el exponente a la variable X7 conforme el valor a la derecha del igual
x7 = 5; x7 **= 2
print(x7) 








